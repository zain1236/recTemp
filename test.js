const convertTime = timeStr => {
    const [time, modifier] = timeStr.split(' ');
    let [hours, minutes] = time.split(':');
    // console.log(hours,minutes);
    if (hours === '12') {
       hours = '00';
    }
    if (modifier === 'PM') {
       hours = parseInt(hours, 10) + 12;
    }
    return `${hours}:${minutes}`;
 };

const isTimeInRange = (timeRange, givenTime) => {
    const [startTimeStr, endTimeStr] = timeRange.split(' - ');
    var startTime = convertTime(startTimeStr);
    var endTime = convertTime(endTimeStr)

    const [hours, minutes,secs] = givenTime.split('_');
    const formattedTime = `${hours.padStart(2, '0')}:${minutes.padStart(2, '0')}:${secs.padStart(2, '0')}`;
    
    console.log(startTime,endTime,formattedTime);
    const timeObj = new Date(`2023-07-05T${formattedTime}`);
    const startTimeObj = new Date(`2023-07-05T${startTime}`);
    const endTimeObj = new Date(`2023-07-05T${endTime}`);
  
    // Check if the time falls within the time range
    return timeObj >= startTimeObj && timeObj <= endTimeObj;
  };
  
  
